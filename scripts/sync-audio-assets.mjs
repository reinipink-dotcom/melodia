#!/usr/bin/env node
// Regenerates the static Metro require() map for bundled MP3 assets.

import fs from 'node:fs';
import path from 'node:path';

const ROOT = path.resolve(import.meta.dirname, '..');
const AUDIO_DIR = path.join(ROOT, 'assets/audio');
const OUTPUT_FILE = path.join(ROOT, 'src/utils/audioAssets.ts');

function walk(dir) {
  if (!fs.existsSync(dir)) return [];
  return fs.readdirSync(dir, { withFileTypes: true }).flatMap((entry) => {
    const absolutePath = path.join(dir, entry.name);
    if (entry.isDirectory()) return walk(absolutePath);
    return entry.isFile() && entry.name.endsWith('.mp3') ? [absolutePath] : [];
  });
}

function toPosixPath(value) {
  return value.split(path.sep).join('/');
}

const assetPaths = walk(AUDIO_DIR)
  .map((absolutePath) => toPosixPath(path.relative(ROOT, absolutePath)))
  .sort((a, b) => a.localeCompare(b));

const maxPathLength = assetPaths.reduce((max, assetPath) => Math.max(max, assetPath.length), 0);
const rows = assetPaths.map((assetPath) => {
  const padding = ' '.repeat(maxPathLength - assetPath.length);
  return `  '${assetPath}':${padding} require('../../${assetPath}'),`;
});

const output = `// Generated by scripts/sync-audio-assets.mjs. Do not edit by hand.
// Metro requires static require() calls for bundled audio assets.

export const AUDIO_ASSETS: Record<string, number> = {
${rows.join('\n')}
};
`;

fs.writeFileSync(OUTPUT_FILE, output);
console.log(`synced ${assetPaths.length} audio assets -> ${path.relative(ROOT, OUTPUT_FILE)}`);
